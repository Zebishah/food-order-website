import mongoose from "mongoose";
const Schema = mongoose.Schema;
let AdminSchema = new Schema({
  userName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

export default mongoose.model("Admin", AdminSchema);
